package org.openmrs.module.rwandaprimarycare;


public class PrimaryCareException extends Exception {

	public PrimaryCareException() {
	    super();
    }

	public PrimaryCareException(String message, Throwable cause) {
	    super(message, cause);
    }

	public PrimaryCareException(String message) {
	    super(message);
    }

	public PrimaryCareException(Throwable cause) {
	    super(cause);
    }
}
